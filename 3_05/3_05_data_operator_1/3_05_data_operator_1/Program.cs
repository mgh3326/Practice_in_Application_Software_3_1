using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace _3_05_data_operator_1
{
    class Program
    {
        static void Main(string[] args)
        {
            int nFirst = 2;
            int nSecond = 7;

            Console.WriteLine("nFirst = {0} ,nSecond ={1}", nFirst, nSecond);
            Console.WriteLine("nFirst + nSecond = {0}", nFirst + nSecond);
            Console.WriteLine("nFirst - nSecond = {0}", nFirst - nSecond);
            Console.WriteLine("nFirst / nSecond = {0}", nFirst / nSecond);
            Console.WriteLine("nFirst * nSecond = {0}", nFirst * nSecond);
            Console.WriteLine("nFirst % nSecond = {0}", nFirst % nSecond);
        }
    }
}
